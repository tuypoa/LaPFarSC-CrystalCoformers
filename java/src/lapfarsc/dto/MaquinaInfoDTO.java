package lapfarsc.dto;

public class MaquinaInfoDTO {

	private Integer infoCodigo;
	private String nome;
	private String valor;
	
	public Integer getInfoCodigo() {
		return infoCodigo;
	}
	public void setInfoCodigo(Integer infoCodigo) {
		this.infoCodigo = infoCodigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
}


